    "Fasting": """
    Complete abstinence is to be observed on all Fridays of the year, Ash Wednesday, the Vigils of Immaculate Conception and Christmas. Partial abstinence is to be observed on Ember Wednesdays and Saturdays and on the Vigil of Pentecost. Days of fast are all the weekdays of Lent, Ember Days, and the Vigil of Pentecost.
    
    If a vigil falls on a Sunday, the law of abstinence and fasting is dispensed that year and is not transferred to the preceding day. General custom allows a double portion of food at the collation on Christmas Eve (jejunium gaudiosum).
    
    On July 25, 1957, Pope Pius XII transferred the fast in the Universal Church from the Vigil of the Assumption to the Vigil of the Immaculate Conception on December 7, even though he had previously abrogated the Mass for the Vigil of the Immaculate Conception. Thus, this day starting in 1957 was a day of mandatory fasting and abstinence. This is preserved in the laws in force in 1962.
